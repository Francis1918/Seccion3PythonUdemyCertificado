#Caracteres especiales
#sirven  para dar un cierto formato a la cadena de texto
# \n -> Nueva línea
print('Hola\nMundo')
# \t -> Tabulación
print('Hola\tMundo')
# comillas simples -> \'
print('Hola \'Mundo\'')
# comillas dobles -> \"
print("Hola \"Mundo\"")
# barra invertida -> \\
print('Hola \\ Mundo')
